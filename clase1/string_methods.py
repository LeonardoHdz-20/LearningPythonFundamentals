# Métodos comunes en cadenas
message = "La edad en México para jubilarse es de 65 años. "

#Strip
message = message.strip()

print("Mensajes sin espacios en blanco: {message}")

#Upper
mayus = message.upper()
print(f"Mensajes en mayúsculas: {mayus}")

#Lower

minus = message.lower()
print(f"Mensajes en minusculas: {minus}")

#Title

title = message.title()
print(f"Mensajes en tipo titulo: {title}")